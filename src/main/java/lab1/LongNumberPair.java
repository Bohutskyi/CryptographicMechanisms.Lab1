package lab1;

public class LongNumberPair {

    private LongNumber first, second;

    LongNumberPair(LongNumber first, LongNumber second) {
        this.first = first;
        this.second = second;
    }

    public LongNumber getFirst() {
        return first;
    }

    public LongNumber getSecond() {
        return second;
    }
}
